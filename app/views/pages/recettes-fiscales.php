    <!-- NOUVELLES MESURES FISCALES -->
    <section class="card">
      <h2>Nouvelles Mesures Fiscales 2025</h2>
      
      <div style="display: grid; gap: 1.5rem; margin-top: 1.5rem;">
        
        <div style="border-left: 5px solid #1565C0; padding: 1rem; background: #E3F2FD;">
          <h3 style="color: #1565C0; margin-top: 0;">Nouveaux Dispositifs Fiscaux</h3>
          <ul class="styled-list">
            <li><strong>Taxe sur les Transactions Mobiles (TTM)</strong> : Taux de 0,5% sur les transferts d'argent (recettes prévisionnelles : 143 Mds Ar)</li>
            <li><strong>Droit d'accise sur les produits nuisibles</strong> à l'environnement et à la santé</li>
            <li><strong>Rationalisation des exonérations</strong> en matière de TVA et de droits d'accise</li>
            <li><strong>Suppression des exonérations</strong> sur certains produits (chaussures de sport, verre de lunetterie, etc.)</li>
          </ul>
        </div>

        <div style="border-left: 5px solid #388E3C; padding: 1rem; background: #E8F5E9;">
          <h3 style="color: #388E3C; margin-top: 0;">Élargissement de l'Assiette Fiscale</h3>
          <ul class="styled-list">
            <li><strong>Retaxation des intérêts bancaires</strong> et des contrats d'assurance à la TVA (+144,23 Mds Ar)</li>
            <li><strong>Soumission de nouveaux produits</strong> au droit d'accise (plastiques, cigarettes électroniques)</li>
            <li><strong>Ajustement des tarifs fiscaux</strong> pour les produits alcooliques et le tabac</li>
            <li><strong>Révision de la TVA</strong> sur les importations et ventes de gaz butanes (taux à 10%)</li>
          </ul>
        </div>

      </div>

      <div style="margin-top: 1.5rem; padding: 1rem; background: #FFF3E0; border-radius: 8px;">
        <p style="margin: 0;"><strong>Impact attendu :</strong> Ces mesures devraient générer des recettes complémentaires de <strong>784,3 milliards d'Ariary</strong>, tout en renforçant les capacités de mobilisation des ressources des Collectivités Territoriales Décentralisées.</p>
      </div>
    </section>

